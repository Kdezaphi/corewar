int		ft_strchri(char *s, unsigned char c)
{
	int		i;

	if (!s)
		return (-1);
	i = 0;
	while (s[i])
	{
		if ((unsigned char)s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
